//estos arrojan valores boleanos

let a = 5;

//operadores relacionales
console.log (a > 3); //true
console.log (a < 3); //false
console.log (a <= 5); //true
console.log (a >= 3); //false

//operadores de igualdad

console.log (a == 5); //true. == son compara el valor y no el tipo de dato
console.log (a != 5); // != se usa para saber sin son diferentes
console.log (a === '5'); // compara el valor y el tipo de dato. False
