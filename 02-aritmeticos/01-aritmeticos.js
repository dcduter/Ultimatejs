// operadores aritmeticos
// +, -, *, /, % (resto de la division)
// ejemplo: 5 + 3 = 8


// declaracion
let a = 3;
let b = 5;

// expresion
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b); // modulo
console.log(a ** b); // potencia

//incrementar
console.log(++a);

console.log(a++);
console.log(a);

//decrementar
console.log(--a);
console.log(a--);
console.log(a);
